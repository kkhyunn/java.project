package com.ict01.grammar02;
class Ex01 {
	public static void main(String[] args) {
		// 논리형 : boolean, true 또는 false를 저장
		// 자료형 변수이름;
		boolean bo1;
		bo1 = true;

		// 변수 호출
		System.out.println(bo1);

		bo1 = false;
		System.out.println(bo1);

		boolean bo2 = true;
		System.out.println(bo2);
	}
}